// Generated by build.rs: reads DIOXUS_ASSET_ROOT from the build environment.
// Only used on WASM to pass the base path to FixedPrefixHistory.
#[cfg(target_arch = "wasm32")]
include!(concat!(env!("OUT_DIR"), "/base_path.rs"));

fn main() {
    // On WASM, use FixedPrefixHistory to work around a wasm-split bug where
    // format!("/{prefix}") produces an empty string in WebHistory::new_inner().
    #[cfg(target_arch = "wasm32")]
    {
        let bp = BASE_PATH.map(|s| s.to_string());
        let history: std::rc::Rc<dyn dioxus::history::History> =
            std::rc::Rc::new(fixed_history::FixedPrefixHistory::new(bp, true));
        let mut vdom = dioxus_core::VirtualDom::new(App);
        vdom.provide_root_context(history);
        dioxus::web::launch::launch_virtual_dom(vdom, dioxus::web::Config::new());
        return;
    }

    #[allow(unreachable_code)]
    {
        #[allow(unused_mut)]
        let mut builder = dioxus::LaunchBuilder::new().with_cfg(server_only! {
            ServeConfig::builder()
                .incremental(
                    dioxus::server::IncrementalRendererConfig::new()
                        .static_dir(
                            std::env::current_exe()
                                .unwrap()
                                .parent()
                                .unwrap()
                                .join("public")
                        )
                        .clear_cache(false)
                )
                .enable_out_of_order_streaming()
        });
        builder.launch(App);
    }
}

#[component]
fn App() -> Element {
    use_init_i18n(|| {
        I18nConfig::new(langid!("en-US"))
            .with_locale((langid!("en-US"), include_str!("i18n/en-US.ftl")))
            .with_locale((langid!("fr-FR"), include_str!("i18n/fr-FR.ftl")))
            .with_locale((langid!("es-ES"), include_str!("i18n/es-ES.ftl")))
            .with_locale((langid!("de-DE"), include_str!("i18n/de-DE.ftl")))
    });

    rsx! {
        Router::<Route> {}
    }
}

#[cfg(feature = "fullstack")]
#[server(endpoint = "static_routes", output = server_fn::codec::Json)]
async fn static_routes() -> Result<Vec<String>, ServerFnError> {
    let mut routes = vec!["/".to_string()];
    for meta in components::COMPONENT_LIST {
        routes.push(format!("/docs/components/{}", meta.name));
        if meta.r#type == ComponentType::Block {
            for variant in meta.variants {
                routes.push(format!("/component/block/{}/{}", meta.name, variant.name));
            }
        }
    }
    Ok(routes)
}

