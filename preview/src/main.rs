#[derive(Copy, Clone, PartialEq)]
pub(crate) enum ComponentType {
    /// Normal componet as default.
    Normal,
    /// Component that render the preview inside an iframe for isolation.
    Block,
}

/// Static metadata for a component — no function pointers.
/// Safe for the shared WASM chunk (doesn't pull in component code).
#[derive(Clone, PartialEq)]
pub(crate) struct ComponentMetadata {
    pub(crate) name: &'static str,
    pub(crate) description: &'static str,
    pub(crate) r#type: ComponentType,
    pub(crate) docs: &'static str,
    pub(crate) api_docs: &'static str,
    pub(crate) component: HighlightedCode,
    pub(crate) style: HighlightedCode,
    pub(crate) variants: &'static [VariantMetadata],
}

/// Metadata for a single variant — no function pointer.
#[derive(Clone, PartialEq)]
pub(crate) struct VariantMetadata {
    pub(crate) name: &'static str,
    pub(crate) description: &'static str,
    pub(crate) rs_highlighted: HighlightedCode,
    pub(crate) css_highlighted: Option<HighlightedCode>,
}

/// A live demo entry passed from generated per-component page functions.
/// Contains the function pointer that pulls in the actual component code.
#[allow(unpredictable_function_pointer_comparisons)]
#[derive(Clone, PartialEq)]
pub(crate) struct DemoEntry {
    pub(crate) name: &'static str,
    pub(crate) component: fn() -> Element,
}

#[derive(Copy, Clone, PartialEq)]
pub(crate) struct HighlightedCode {
    pub(crate) light: &'static str,
    pub(crate) dark: &'static str,
}

